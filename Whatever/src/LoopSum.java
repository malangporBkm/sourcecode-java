
public class LoopSum {

    public static void main(String[] args) {
        int sum = 0;
        for (int i = 0; i < 11; i++) {
            sum = sum + i;
        }
        System.out.println("sum==" + sum);

        for (int i = 0; i < 10; i++) {
            System.out.println("********");
        }
    }

}
